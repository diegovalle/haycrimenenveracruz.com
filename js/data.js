function nationalData(data, str) {
    var homRate = [];
    
    for (var i = 0; i < data.items.length; i++) {
        //javascript automatically takes care 
        //of incrementing the year
	if(!(data.items[i].count===null)) 
            data.items[i].count = (+data.items[i].count) /// +data.items[i].population * 100000) * 12
        else
            data.items[i].count = null
        homRate.push({x: new Date(+data.items[i].year, +data.items[i].month-1, 01), 
                      y: data.items[i].count,
                      //count: +data.items[i].count,
                      population: +data.items[i].population});
    }
    
               return [
                   {
                       values: homRate,
                       key: str,
                       color: '#111'
                   }
               ];
}

